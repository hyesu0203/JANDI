# 1. 기초 - 출력

# [1] Hello 출력하기
print('Hello')
print("Hello")

# [2] Hello World 출력하기
print("Hello World")
print('Hello World')

# [3] Hello World_두 줄에 걸쳐 줄을 바꿔 출력하기
print('''Hello
World''')
print("""Hello
World""")

# [4] 'Hello' (단, 작은 따옴표도 함께 출력한다.)
print("'Hello World'")

# [5] "Hello World" (단, 큰 따옴표도 함께 출력한다.)
print('"Hello World"')

# [6] "!@#$%^&*()" (단, 큰 따옴표도 함께 출력한다.)
print('"!@#$%^&*()"')

# [7] "C:\Download\hello.cpp" (단, 큰 따옴표도 함께 출력한다.)
print('"C\\Download\\hello.cpp"')

''' [8] ┌┬┐
        ├┼┤
        └┴┘ 출력하기'''
print('''┌┬┐
├┼┤
└┴┘ ''')
